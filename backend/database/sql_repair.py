"""
SQL Repair Engine

Automatically repairs common SQL mistakes
generated by LLMs.

Project:
AI Data Analyst
Author:
Sanket Kabariya
"""

from __future__ import annotations

import re

from backend.database.schema import schema_manager


class SQLRepairEngine:

    def repair(
        self,
        sql: str,
    ) -> str:
        """
        Repair common SQL mistakes.
        """

        schema = schema_manager.get_database_schema()

        repaired_sql = sql

        all_columns = []

        for table in schema.values():

            for column in table["columns"]:

                all_columns.append(column["column_name"])

        for column in all_columns:

            # Only repair underscore versions
            underscore = column.replace(" ", "_")

            if underscore == column:
                continue

            repaired_sql = repaired_sql.replace(
                underscore,
                f'"{column}"'
            )

        return repaired_sql


sql_repair = SQLRepairEngine()